#include <stdio.h>
int main(){
    unsigned int e = 4000000000;
    unsigned int d = 3000000000;
    char q = 120;
    long long  p =  500000000000000;
    //Fills in zeroes for unused decimal places 
    double b = 5.268;
    char t = 256;
    char y = -256;
    printf("e = %d d = %d q = %c p = %lld b = %lf t = %c y = %c\n", e,d,q,p,b,t,y);
    return 0;
}